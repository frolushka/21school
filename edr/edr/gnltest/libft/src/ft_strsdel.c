#include "libft.h"

void	ft_strsdel(char ***as)
{
	int	i;

	i = -1;
	if (!as || !*as || !**as)
		return ;
	while ((*as)[++i])
		free((*as)[i]);
	free(*as);
	*as = NULL;
}
