#include "ddd.h"

t_vec2	*to_screen(t_vec3 *w)
{
	t_vec2	*res;

	res = vec2_init();
	res->x = w->x * 0.7 + w->y * 0 + w->z * -0.71;
	res->y = w->x * 0.40 - w->y * 0.816 + w->z * 0.40;
	return (res);
}