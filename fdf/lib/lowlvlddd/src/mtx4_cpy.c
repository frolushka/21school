#include "lowlvlddd.h"

t_mtx4	*mtx4_cpy(const t_mtx4 *src)
{
	t_mtx4	*res;
	int		i;

	res = mtx4_init(0.0f);
	i = -1;
	while (++i < 16)
		res->val[i] = src->val[i];
	return (res);
}
